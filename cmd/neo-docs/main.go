func toolsPage(specs []llm.ToolSpec) string {
	var b strings.Builder
	b.WriteString(generatedHeader)
	b.WriteString("# Tools\n\n")
	b.WriteString("Neo exposes a small built-in tool surface to the model.\n\n")
	b.WriteString("| Tool | Description |\n")
	b.WriteString("| --- | --- |\n")
	for _, spec := range specs {
		fmt.Fprintf(&b, "| `%s` | %s |\n", spec.Name, escapeTable(spec.Description))
	}
	b.WriteString("\n## Schemas\n\n")
	for _, spec := range specs {
		fmt.Fprintf(&b, "### `%s`\n\n", spec.Name)
		b.WriteString(spec.Description)
		b.WriteString("\n\n")
		schema, _ := json.MarshalIndent(spec.InputSchema, "", "  ")
		b.WriteString("```json\n")
		b.Write(schema)
		b.WriteString("\n```\n\n")
	}
	return strings.TrimRight(b.String(), "\n") + "\n"
}

