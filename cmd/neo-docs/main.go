package main

import (
	"bytes"
	"encoding/json"
	"flag"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"time"

	"github.com/owainlewis/neo/internal/llm"
	"github.com/owainlewis/neo/internal/tools"
)

const generatedHeader = "<!-- Generated by `go run ./cmd/neo-docs`. Do not edit by hand. -->\n\n"

type page struct {
	Path    string
	Content string
}

func main() {
	check := flag.Bool("check", false, "fail if generated developer docs are out of date")
	flag.Parse()

	pages, err := buildPages()
	if err != nil {
		fatal(err)
	}
	if *check {
		if err := checkPages(pages); err != nil {
			fatal(err)
		}
		return
	}
	if err := writePages(pages); err != nil {
		fatal(err)
	}
	for _, page := range pages {
		fmt.Printf("generated %s\n", page.Path)
	}
}

func fatal(err error) {
	fmt.Fprintln(os.Stderr, err)
	os.Exit(1)
}

func buildPages() ([]page, error) {
	configYAML, err := os.ReadFile(filepath.Join("internal", "config", "defaults", "neo.yaml"))
	if err != nil {
		return nil, fmt.Errorf("read default config: %w", err)
	}
	specs := toolSpecs()
	return []page{
		{Path: filepath.Join("docs", "developer", "index.md"), Content: indexPage()},
		{Path: filepath.Join("docs", "developer", "architecture.md"), Content: architecturePage()},
		{Path: filepath.Join("docs", "developer", "cli.md"), Content: cliPage()},
		{Path: filepath.Join("docs", "developer", "config.md"), Content: configPage(string(configYAML))},
		{Path: filepath.Join("docs", "developer", "sessions.md"), Content: sessionsPage()},
		{Path: filepath.Join("docs", "developer", "tools.md"), Content: toolsPage(specs)},
		{Path: filepath.Join("docs", "developer", "prompt-caching.md"), Content: promptCachingPage()},
	}, nil
}

func toolSpecs() []llm.ToolSpec {
	reg := tools.NewRegistry(
		tools.Bash{Timeout: 2 * time.Minute},
		tools.ReadFile{},
		tools.WriteFile{},
		tools.EditFile{},
		tools.Grep{Root: "."},
		tools.Glob{Root: "."},
	)
	specs := reg.Specs()
	sort.Slice(specs, func(i, j int) bool { return specs[i].Name < specs[j].Name })
	return specs
}

func writePages(pages []page) error {
	for _, page := range pages {
		if err := os.MkdirAll(filepath.Dir(page.Path), 0o755); err != nil {
			return fmt.Errorf("create %s: %w", filepath.Dir(page.Path), err)
		}
		if err := os.WriteFile(page.Path, []byte(page.Content), 0o644); err != nil {
			return fmt.Errorf("write %s: %w", page.Path, err)
		}
	}
	return nil
}

func checkPages(pages []page) error {
	var stale []string
	for _, page := range pages {
		got, err := os.ReadFile(page.Path)
		if err != nil {
			stale = append(stale, page.Path)
			continue
		}
		if !bytes.Equal(got, []byte(page.Content)) {
			stale = append(stale, page.Path)
		}
	}
	if len(stale) > 0 {
		return fmt.Errorf("developer docs are out of date; run `go run ./cmd/neo-docs`: %s", strings.Join(stale, ", "))
	}
	return nil
}

